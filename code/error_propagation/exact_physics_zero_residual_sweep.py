def validate_args(args):
    if any(ratio < 1 for ratio in args.ratios):
        raise ValueError("All ratios must be positive integers.")
    if args.observation_stride < 1:
        raise ValueError("observation_stride must be positive.")
    if args.num_epochs < 1 or args.chunk_size < 1:
        raise ValueError("num_epochs and chunk_size must be positive.")
    if args.num_epochs % args.chunk_size != 0:
        raise ValueError("num_epochs must be divisible by chunk_size.")
    if args.step_size <= 0:
        raise ValueError("step_size must be positive.")
    if args.residual_regularization < 0:
        raise ValueError("residual_regularization cannot be negative.")


def main():
    args = parse_args()
    validate_args(args)
    args.output_dir.mkdir(parents=True, exist_ok=True)
    data = make_reference_data(
        args.observation_stride, args.reference_rtol, args.reference_atol
    )
    continuous_baseline = make_zero_correction_continuous_baseline(
        data, args.evaluation_rtol, args.evaluation_atol
    )
    progress(
        f"[setup] observation_dt={data.observation_dt:g}, "
        f"train_points={data.train.shape[1]}, output={args.output_dir}"
    )

    trained_runs = {}
    total_runs = len(args.solvers) * len(args.ratios) * len(args.seeds)
    run_index = 0
    for solver_name in args.solvers:
        method = SOLVERS[solver_name]
        for ratio in args.ratios:
            for seed in args.seeds:
                run_index += 1
                progress(
                    f"[train {run_index}/{total_runs}] solver={solver_name}, "
                    f"ratio={ratio}, h={data.observation_dt / ratio:g}, seed={seed}"
                )
                trained = train_model(
                    seed=seed,
                    method=method,
                    ratio=ratio,
                    data=data,
                    num_epochs=args.num_epochs,
                    step_size=args.step_size,
                    chunk_size=args.chunk_size,
                    patience=args.patience,
                    min_delta=args.min_delta,
                    hidden_width=args.hidden_width,
                    hidden_scale=args.hidden_scale,
                    residual_regularization=args.residual_regularization,
                )
                trained_runs[(seed, solver_name, ratio)] = trained
                progress(
                    f"[train {run_index}/{total_runs}] "
                    f"initial={trained['initial_loss']:.3e}, "
                    f"best={trained['best_loss']:.3e}, "
                    f"epoch={trained['best_epoch']}, "
                    f"grad={trained['best_gradient_norm']:.3e}"
                )

    rows = []
    curves = {}
    history_rows = []
    for evaluation_index, (key, trained) in enumerate(trained_runs.items(), start=1):
        seed, solver_name, ratio = key
        progress(
            f"[eval {evaluation_index}/{total_runs}] "
            f"solver={solver_name}, ratio={ratio}, seed={seed}"
        )
        row, run_curves = evaluate_model(
            trained,
            data,
            continuous_baseline,
            args.evaluation_rtol,
            args.evaluation_atol,
        )
        rows.append(row)
        curves[key] = run_curves
        for history_item in trained["history"]:
            history_rows.append(
                {
                    "seed": seed,
                    "training_method": solver_name,
                    "ratio": ratio,
                    "h_model": trained["h_model"],
                    **history_item,
                }
            )
        progress(
            f"[eval {evaluation_index}/{total_runs}] "
            f"matched={row['matched_train_mse']:.3e}, "
            f"continuous={row['continuous_train_mse']:.3e}, "
            f"correction_rel={row['correction_relative_to_true_vf']:.3e}"
        )

    group_rows = make_group_summary(rows)
    write_csv(rows, args.output_dir / "summary.csv")
    write_csv(group_rows, args.output_dir / "group_summary.csv")
    write_csv(history_rows, args.output_dir / "training_history.csv")
    with (args.output_dir / "details.pkl").open("wb") as handle:
        pickle.dump(
            {
                "trained": trained_runs,
                "rows": rows,
                "group_rows": group_rows,
                "curves": curves,
            },
            handle,
        )

    metadata = {
        "created_utc": datetime.now(timezone.utc).isoformat(),
        "description": "Exact physics plus initially zero neural correction sweep",
        "true_parameters": {"a": A, "b": B, "r": R, "z": Z, "c": C},
        "train_horizon": T_TRAIN,
        "extrapolation_horizon": T_EXTRAPOLATE,
        "base_dt": BASE_DT,
        "jax_enable_x64": bool(jax.config.x64_enabled),
        "arguments": {
            key: str(value) if isinstance(value, Path) else value
            for key, value in vars(args).items()
        },
    }
    with (args.output_dir / "metadata.json").open("w") as handle:
        json.dump(metadata, handle, indent=2)

    if args.plots:
        plot_correction_vs_stepsize(group_rows, args.output_dir)
        plot_matched_vs_continuous(rows, args.output_dir)
    progress(f"[done] results written to {args.output_dir}")


if __name__ == "__main__":
    main()
