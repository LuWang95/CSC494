"""Exact-physics UDE sweep with an initially zero neural correction.

The data are generated by the complete Lotka--Volterra system. The learned
model contains that exact vector field plus a two-output neural correction,

    f_model(y) = f_true(y) + correction_nn(y).

The correction network is initialized to output exactly zero by using random
hidden layers and a zero final layer. Thus the initial continuous-time model is
already correct. Any nonzero correction learned from a fixed-step rollout loss
is a candidate solver-induced compensation.

This script mirrors the original fixed-physics sweep: full-batch Adam training,
fixed-step Euler/Heun/RK4 rollouts, paired seeds, and tight Diffrax evaluation.
It additionally reports the initial matched-solver loss, learned-correction
norms, continuous-vs-matched losses, and the leading Euler IMDE alignment.
"""

